        $("#trackBooking").on("click", function() {
            const book_id = $("#book_id").val();
            $.get("/track-booking/" + book_id, function(data, status) {
                var obj = jQuery.parseJSON(data);

                $("#BookIdModal").text(obj.book_id);
                $("#statusModal").text(obj.status);
                $("#firstNameModal").val(obj.first_name);
                $("#lastNameModal").val(obj.last_name);
                $("#CheckInModal").val(obj.check_in);
                $("#numberModal").val(obj.number);
                $("#emailModal").val(obj.email);
                $("#editUser").modal("show");
            });
